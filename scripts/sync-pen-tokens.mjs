#!/usr/bin/env node

import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const repoRoot = process.cwd();
const penPath = resolve(repoRoot, 'design/design-system.pen');
const outputJsonPath = resolve(repoRoot, 'design/tokens/design-tokens.generated.json');
const outputMdPath = resolve(repoRoot, 'design/tokens/design-tokens.generated.md');
const outputCssPath = resolve(repoRoot, 'src/styles/tokens/pen-tokens.generated.css');
const outputTsPath = resolve(repoRoot, 'src/styles/tokens/pen-tokens.generated.ts');
const outputDesignGuideJsPath = resolve(
  repoRoot,
  'design-guide/forma-studio/src/styles/themes/pen-tokens.generated.js',
);

const pen = JSON.parse(readFileSync(penPath, 'utf8'));

const normalizeEntries = (rawValue) => {
  if (Array.isArray(rawValue)) {
    return rawValue.map((entry) => ({
      value: entry?.value ?? null,
      theme: entry?.theme ?? null,
    }));
  }

  return [
    {
      value: rawValue ?? null,
      theme: null,
    },
  ];
};

const variables = Object.entries(pen.variables ?? {})
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([token, variable]) => {
    const entries = normalizeEntries(variable?.value);
    const unscoped = entries.filter((entry) => entry.theme === null);
    const scoped = entries.filter((entry) => entry.theme !== null);

    return {
      token,
      type: variable?.type ?? 'unknown',
      defaultValue: (unscoped[0]?.value ?? entries[0]?.value ?? null),
      unscopedValues: unscoped.map((entry) => entry.value),
      variants: scoped.map((entry) => ({
        when: entry.theme,
        value: entry.value,
      })),
      hasAmbiguousFallback: unscoped.length > 1,
    };
  });

const data = {
  source: {
    file: 'design/design-system.pen',
    version: pen.version ?? null,
  },
  themeAxes: pen.themes ?? {},
  variables,
  warnings: {
    ambiguousFallbackTokens: variables
      .filter((variable) => variable.hasAmbiguousFallback)
      .map((variable) => variable.token),
  },
};

const defaultTokenMap = Object.fromEntries(
  variables
    .filter((variable) => variable.defaultValue !== null)
    .map((variable) => [variable.token, variable.defaultValue]),
);

const mappedPencilTokens = {
  primary: defaultTokenMap['--primary'],
  primaryForeground: defaultTokenMap['--primary-foreground'],
  secondary: defaultTokenMap['--secondary'],
  secondaryForeground: defaultTokenMap['--secondary-foreground'],
  background: defaultTokenMap['--background'],
  foreground: defaultTokenMap['--foreground'],
  card: defaultTokenMap['--card'],
  cardForeground: defaultTokenMap['--card-foreground'],
  popover: defaultTokenMap['--popover'],
  popoverForeground: defaultTokenMap['--popover-foreground'],
  muted: defaultTokenMap['--muted'],
  mutedForeground: defaultTokenMap['--muted-foreground'],
  accent: defaultTokenMap['--accent'],
  accentForeground: defaultTokenMap['--accent-foreground'],
  destructive: defaultTokenMap['--destructive'],
  border: defaultTokenMap['--border'],
  input: defaultTokenMap['--input'],
  ring: defaultTokenMap['--ring'],
  sidebar: defaultTokenMap['--sidebar'],
  sidebarForeground: defaultTokenMap['--sidebar-foreground'],
  sidebarBorder: defaultTokenMap['--sidebar-border'],
  sidebarAccent: defaultTokenMap['--sidebar-accent'],
  sidebarAccentForeground: defaultTokenMap['--sidebar-accent-foreground'],
  sidebarPrimary: defaultTokenMap['--sidebar-primary'],
  sidebarPrimaryForeground: defaultTokenMap['--sidebar-primary-foreground'],
  sidebarRing: defaultTokenMap['--sidebar-ring'],
};

const md = [
  '# Generated Design Tokens',
  '',
  'Source of truth: `design/design-system.pen`',
  '',
  'This file is auto-generated by `pnpm tokens:sync`.',
  '',
  '## Theme Axes',
  '',
  ...Object.entries(data.themeAxes).map(([axis, values]) => `- \`${axis}\`: ${values.map((value) => `\`${value}\``).join(', ')}`),
  '',
  '## Default Token Values',
  '',
  '| Token | Default |',
  '| --- | --- |',
  ...variables.map((variable) => `| \`${variable.token}\` | \`${variable.defaultValue}\` |`),
];

const ambiguous = data.warnings.ambiguousFallbackTokens;
if (ambiguous.length > 0) {
  md.push(
    '',
    '## Ambiguous Fallback Warning',
    '',
    'These tokens have multiple unscoped values in `design-system.pen`.',
    'The first unscoped value is used as `defaultValue` in generated outputs.',
    '',
    ...ambiguous.map((token) => `- \`${token}\``),
  );
}

md.push('', '## Variant Entries', '');
for (const variable of variables) {
  if (variable.variants.length === 0) {
    continue;
  }

  md.push(`### \`${variable.token}\``, '', '| Condition | Value |', '| --- | --- |');
  for (const variant of variable.variants) {
    const condition = Object.entries(variant.when)
      .map(([key, value]) => `${key}=${value}`)
      .join(', ');
    md.push(`| \`${condition}\` | \`${variant.value}\` |`);
  }
  md.push('');
}

const cssLines = [
  '/* Auto-generated by scripts/sync-pen-tokens.mjs. Do not edit manually. */',
  ':root {',
  ...Object.entries(defaultTokenMap).map(([token, value]) => `  ${token}: ${value};`),
  '}',
  '',
];

const tsLines = [
  '// Auto-generated by scripts/sync-pen-tokens.mjs. Do not edit manually.',
  '',
  `export const PEN_TOKEN_SOURCE = 'design/design-system.pen' as const;`,
  `export const PEN_TOKEN_SOURCE_VERSION = ${JSON.stringify(data.source.version)} as const;`,
  '',
  `export const PEN_THEME_AXES = ${JSON.stringify(data.themeAxes, null, 2)} as const;`,
  '',
  `export const PEN_DEFAULT_TOKENS = ${JSON.stringify(defaultTokenMap, null, 2)} as const;`,
  '',
  'export type PenTokenName = keyof typeof PEN_DEFAULT_TOKENS;',
  '',
  'export function getPenToken(token: PenTokenName): string {',
  '  return PEN_DEFAULT_TOKENS[token];',
  '}',
  '',
];

const designGuideJsLines = [
  '// Auto-generated by scripts/sync-pen-tokens.mjs. Do not edit manually.',
  '',
  `export const PEN_DEFAULT_TOKENS = ${JSON.stringify(defaultTokenMap, null, 2)};`,
  '',
  `export const pencilTokens = ${JSON.stringify(mappedPencilTokens, null, 2)};`,
  '',
  'export default pencilTokens;',
  '',
];

mkdirSync(dirname(outputJsonPath), { recursive: true });
mkdirSync(dirname(outputCssPath), { recursive: true });
mkdirSync(dirname(outputDesignGuideJsPath), { recursive: true });
writeFileSync(outputJsonPath, `${JSON.stringify(data, null, 2)}\n`, 'utf8');
writeFileSync(outputMdPath, `${md.join('\n')}\n`, 'utf8');
writeFileSync(outputCssPath, `${cssLines.join('\n')}`, 'utf8');
writeFileSync(outputTsPath, `${tsLines.join('\n')}`, 'utf8');
writeFileSync(outputDesignGuideJsPath, `${designGuideJsLines.join('\n')}`, 'utf8');

console.log(`Generated: ${outputJsonPath}`);
console.log(`Generated: ${outputMdPath}`);
console.log(`Generated: ${outputCssPath}`);
console.log(`Generated: ${outputTsPath}`);
console.log(`Generated: ${outputDesignGuideJsPath}`);
