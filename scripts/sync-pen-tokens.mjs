#!/usr/bin/env node
/**
 * Generate token artifacts from design/design-system.pen for runtime usage and documentation.
 */

import fs from 'node:fs';
import path from 'node:path';

const workspaceRoot = process.cwd();
const sourcePath = path.join(workspaceRoot, 'design', 'design-system.pen');
const designTokensDirectory = path.join(workspaceRoot, 'design', 'tokens');
const generatedJsonPath = path.join(designTokensDirectory, 'design-tokens.generated.json');
const generatedMarkdownPath = path.join(designTokensDirectory, 'design-tokens.generated.md');
const runtimeCssPath = path.join(workspaceRoot, 'src', 'styles', 'tokens', 'design-tokens.css');
const runtimeTsPath = path.join(workspaceRoot, 'src', 'styles', 'tokens', 'design-tokens.ts');

function normalizeValueEntries(rawValue) {
  if (Array.isArray(rawValue)) {
    return rawValue;
  }
  return [{ value: rawValue }];
}

function getLightValue(entries) {
  const lightEntry = entries.find(
    (item) => !item.theme || Object.keys(item.theme).length === 0 || item.theme.Mode === 'Light',
  );
  if (lightEntry) {
    return lightEntry.value;
  }
  return entries[0]?.value;
}

function getDarkValue(entries) {
  const darkEntry = entries.find((item) => item.theme?.Mode === 'Dark');
  return darkEntry?.value;
}

function toQuotedJsString(value) {
  return `'${String(value).replaceAll('\\', '\\\\').replaceAll('\'', '\\\'')}'`;
}

function main() {
  const penDocument = JSON.parse(fs.readFileSync(sourcePath, 'utf8'));
  const variables = penDocument.variables ?? {};
  const sortedTokenNames = Object.keys(variables).sort((a, b) => a.localeCompare(b));

  const tokenRecords = {};
  const rootCssLines = [];
  const darkCssLines = [];

  for (const tokenName of sortedTokenNames) {
    const definition = variables[tokenName];
    const entries = normalizeValueEntries(definition.value);
    const lightValue = getLightValue(entries);
    const darkValue = getDarkValue(entries);

    tokenRecords[tokenName] = {
      dark: darkValue ?? null,
      light: lightValue,
      type: definition.type,
    };

    rootCssLines.push(`  ${tokenName}: ${lightValue};`);
    if (darkValue && darkValue !== lightValue) {
      darkCssLines.push(`  ${tokenName}: ${darkValue};`);
    }
  }

  const generatedAt = new Date().toISOString();
  const generatedPayload = {
    generatedAt,
    source: 'design/design-system.pen',
    themes: penDocument.themes ?? {},
    tokenCount: sortedTokenNames.length,
    tokens: tokenRecords,
  };

  fs.mkdirSync(designTokensDirectory, { recursive: true });

  fs.writeFileSync(
    generatedJsonPath,
    `${JSON.stringify(generatedPayload, null, 2)}\n`,
    'utf8',
  );

  const markdownTableRows = sortedTokenNames
    .map((tokenName) => {
      const token = tokenRecords[tokenName];
      return `| \`${tokenName}\` | \`${token.type}\` | \`${token.light}\` | \`${token.dark ?? '-'}\` |`;
    })
    .join('\n');

  const markdownDocument = `# Generated Design Tokens

Source: \`design/design-system.pen\`  
Generated: \`${generatedAt}\`  
Token count: \`${sortedTokenNames.length}\`

## Theme Axes

\`\`\`json
${JSON.stringify(penDocument.themes ?? {}, null, 2)}
\`\`\`

## Token Values

| Token | Type | Light | Dark |
| --- | --- | --- | --- |
${markdownTableRows}
`;

  fs.writeFileSync(generatedMarkdownPath, markdownDocument, 'utf8');

  const cssDocument = `/* Generated from design/design-system.pen by pnpm tokens:sync. Do not edit manually. */
:root {
${rootCssLines.join('\n')}
}
${darkCssLines.length > 0 ? `\n.dark {\n${darkCssLines.join('\n')}\n}\n` : '\n'}
`;
  fs.writeFileSync(runtimeCssPath, cssDocument, 'utf8');

  const lightTokenEntries = sortedTokenNames
    .map((tokenName) => `  ${toQuotedJsString(tokenName)}: ${toQuotedJsString(tokenRecords[tokenName].light)},`)
    .join('\n');
  const darkTokenEntries = sortedTokenNames
    .filter((tokenName) => tokenRecords[tokenName].dark !== null)
    .map((tokenName) => `  ${toQuotedJsString(tokenName)}: ${toQuotedJsString(tokenRecords[tokenName].dark)},`)
    .join('\n');

  const tsDocument = `/**
 * Generated token constants from design/design-system.pen.
 * This file is auto-generated by pnpm tokens:sync.
 */

export const DESIGN_TOKEN_SOURCE = 'design/design-system.pen' as const;

export const DESIGN_TOKENS = {
${lightTokenEntries}
} as const;

export const DESIGN_DARK_TOKENS = {
${darkTokenEntries}
} as const;

export type DesignTokenName = keyof typeof DESIGN_TOKENS;

export function getDesignToken(token: DesignTokenName): string {
  return DESIGN_TOKENS[token];
}
`;

  fs.writeFileSync(runtimeTsPath, tsDocument, 'utf8');

  console.log('Token sync complete.');
  console.log(`- ${path.relative(workspaceRoot, generatedJsonPath)}`);
  console.log(`- ${path.relative(workspaceRoot, generatedMarkdownPath)}`);
  console.log(`- ${path.relative(workspaceRoot, runtimeCssPath)}`);
  console.log(`- ${path.relative(workspaceRoot, runtimeTsPath)}`);
}

main();
