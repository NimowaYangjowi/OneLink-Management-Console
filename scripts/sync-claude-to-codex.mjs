#!/usr/bin/env node
// Generates AGENTS.md for Codex by syncing CLAUDE.md, Claude skills, rules, and stop-hook metadata.

import { promises as fs } from 'node:fs';
import path from 'node:path';

const PROJECT_ROOT = process.cwd();
const CLAUDE_MD_PATH = path.join(PROJECT_ROOT, 'CLAUDE.md');
const CLAUDE_SETTINGS_PATH = path.join(PROJECT_ROOT, '.claude', 'settings.json');
const CLAUDE_RULES_DIR = path.join(PROJECT_ROOT, '.claude', 'rules');
const CLAUDE_SKILLS_DIR = path.join(PROJECT_ROOT, '.claude', 'skills');
const OUTPUT_PATH = path.join(PROJECT_ROOT, 'AGENTS.md');

async function readText(filePath) {
  return fs.readFile(filePath, 'utf8');
}

async function exists(filePath) {
  try {
    await fs.access(filePath);
    return true;
  } catch {
    return false;
  }
}

function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---\n?/);
  if (!match) {
    return {};
  }

  const raw = match[1];
  const parsed = {};

  for (const line of raw.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) {
      continue;
    }

    const separatorIndex = trimmed.indexOf(':');
    if (separatorIndex === -1) {
      continue;
    }

    const key = trimmed.slice(0, separatorIndex).trim();
    let value = trimmed.slice(separatorIndex + 1).trim();

    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }

    parsed[key] = value;
  }

  return parsed;
}

function compactWhitespace(value) {
  return value.replace(/\s+/g, ' ').trim();
}

async function getSkills() {
  if (!(await exists(CLAUDE_SKILLS_DIR))) {
    return [];
  }

  const entries = await fs.readdir(CLAUDE_SKILLS_DIR, { withFileTypes: true });
  const skills = [];

  for (const entry of entries) {
    if (!entry.isDirectory()) {
      continue;
    }

    const skillPath = path.join(CLAUDE_SKILLS_DIR, entry.name, 'SKILL.md');
    if (!(await exists(skillPath))) {
      continue;
    }

    const content = await readText(skillPath);
    const frontmatter = parseFrontmatter(content);

    const name = compactWhitespace(frontmatter.name || entry.name);
    const description = compactWhitespace(
      frontmatter.description || 'No description provided.',
    );

    skills.push({
      name,
      description,
      relativePath: path.posix.join('.claude', 'skills', entry.name, 'SKILL.md'),
    });
  }

  return skills.sort((a, b) => a.name.localeCompare(b.name));
}

async function getRuleSummaries() {
  if (!(await exists(CLAUDE_RULES_DIR))) {
    return [];
  }

  const files = await fs.readdir(CLAUDE_RULES_DIR, { withFileTypes: true });
  const summaries = [];

  for (const file of files) {
    if (!file.isFile() || !file.name.endsWith('.md')) {
      continue;
    }

    const rulePath = path.join(CLAUDE_RULES_DIR, file.name);
    const content = await readText(rulePath);
    const firstHeading =
      content
        .split('\n')
        .map((line) => line.trim())
        .find((line) => line.startsWith('# ')) || '# (Untitled)';

    summaries.push({
      relativePath: path.posix.join('.claude', 'rules', file.name),
      heading: firstHeading.replace(/^#\s*/, '').trim(),
    });
  }

  return summaries.sort((a, b) => a.relativePath.localeCompare(b.relativePath));
}

async function getStopHookCommand() {
  if (!(await exists(CLAUDE_SETTINGS_PATH))) {
    return null;
  }

  const content = await readText(CLAUDE_SETTINGS_PATH);
  let parsed;

  try {
    parsed = JSON.parse(content);
  } catch {
    return null;
  }

  const stopHooks = parsed?.hooks?.Stop;
  if (!Array.isArray(stopHooks)) {
    return null;
  }

  for (const matcher of stopHooks) {
    const hooks = matcher?.hooks;
    if (!Array.isArray(hooks)) {
      continue;
    }

    for (const hook of hooks) {
      if (hook?.type === 'command' && typeof hook.command === 'string') {
        return hook.command;
      }
    }
  }

  return null;
}

async function main() {
  if (!(await exists(CLAUDE_MD_PATH))) {
    throw new Error('CLAUDE.md not found. Nothing to bridge.');
  }

  const [claudeMd, skills, ruleSummaries, stopHookCommand] = await Promise.all([
    readText(CLAUDE_MD_PATH),
    getSkills(),
    getRuleSummaries(),
    getStopHookCommand(),
  ]);

  const lines = [];

  lines.push('# AGENTS.md');
  lines.push('');
  lines.push(
    '> This file is generated by `scripts/sync-claude-to-codex.mjs`. Edit `CLAUDE.md` or `.claude/*`, then run `pnpm bridge:codex`.',
  );
  lines.push('');
  lines.push('## Codex Bridge');
  lines.push('');
  lines.push(
    '- Source of truth: `CLAUDE.md`, `.claude/rules/*`, `.claude/skills/*`, `.claude/settings.json`.',
  );
  lines.push('- Regenerate this file with: `pnpm bridge:codex`.');
  lines.push(
    '- Claude `Stop` hook bridge for Codex: run `pnpm codex:stop-hook` (manual) or `pnpm codex:bridge-cli -- <codex args>` (CLI wrapper).',
  );
  if (stopHookCommand) {
    lines.push(`- Synced Claude stop command: \`${stopHookCommand}\`.`);
  }
  lines.push('');
  lines.push('## Skills');
  lines.push('');
  lines.push(
    'A skill is a set of local instructions to follow that is stored in a `SKILL.md` file.',
  );
  lines.push('### Available skills');

  if (skills.length === 0) {
    lines.push('- No skills found under `.claude/skills`.');
  } else {
    for (const skill of skills) {
      lines.push(`- ${skill.name}: ${skill.description} (file: ${skill.relativePath})`);
    }
  }

  lines.push('### How to use skills');
  lines.push(
    '- Trigger rules: If the user names a skill (with `$SkillName` or plain text), or the task clearly matches a skill description, you must use that skill in this turn.',
  );
  lines.push(
    '- Progressive disclosure: Read only enough from each `SKILL.md` and its references/scripts to complete the task.',
  );
  lines.push(
    '- Fallback: If a skill path is missing or unreadable, say so briefly and continue with the best fallback.',
  );
  lines.push('');
  lines.push('## Claude Rule Files');
  lines.push('');

  if (ruleSummaries.length === 0) {
    lines.push('- No rule files found under `.claude/rules`.');
  } else {
    for (const rule of ruleSummaries) {
      lines.push(`- \`${rule.relativePath}\` - ${rule.heading}`);
    }
  }

  lines.push('');
  lines.push('## Synced From `CLAUDE.md`');
  lines.push('');
  lines.push(claudeMd.trimEnd());
  lines.push('');

  await fs.writeFile(OUTPUT_PATH, `${lines.join('\n')}\n`, 'utf8');
  console.log(`Generated ${path.relative(PROJECT_ROOT, OUTPUT_PATH)}.`);
}

main().catch((error) => {
  console.error(error.message);
  process.exit(1);
});
